function zyboTranslate(lang) {
  const page = document.body.dataset.page;
  const common = ZYBO_TRANSLATIONS[lang]?.common || {};
  const current = ZYBO_TRANSLATIONS[lang]?.pages?.[page] || {};
  const getValue = (key) => current[key] ?? common[key] ?? null;

  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = getValue(element.dataset.i18n);
    if (value !== null) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = getValue(element.dataset.i18nPlaceholder);
    if (value !== null) element.placeholder = value;
  });

  const title = document.querySelector("[data-i18n-title]");
  if (title) {
    const value = getValue(title.dataset.i18nTitle);
    if (value !== null) document.title = value;
  }

  const meta = document.querySelector("[data-i18n-meta]");
  if (meta) {
    const value = getValue(meta.dataset.i18nMeta);
    if (value !== null) meta.content = value;
  }

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === lang);
  });
}

window.ZYBO_I18N = {
  t(key) {
    const lang = localStorage.getItem("zybo-lang") || "fr";
    return ZYBO_TRANSLATIONS[lang]?.pages?.[document.body.dataset.page]?.[key]
      ?? ZYBO_TRANSLATIONS[lang]?.common?.[key]
      ?? key;
  }
};

document.addEventListener("DOMContentLoaded", () => {
  const initialLang = localStorage.getItem("zybo-lang") || "fr";
  zyboTranslate(initialLang);

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      localStorage.setItem("zybo-lang", button.dataset.lang);
      zyboTranslate(button.dataset.lang);
    });
  });
});
